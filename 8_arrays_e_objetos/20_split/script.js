// Metodos de STRING SPLIT 

let frase = "Testando o método split";

// Separa a string e devolve um array
// Dentro do split deve ser colocado o tipo de separador que irá dividir a string e transformar em array
console.log(frase.split(" "));

// Também pode atribuir esse array a uma variável
let palavras = frase.split(" ");
console.log(palavras);